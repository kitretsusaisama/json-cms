// Translation function
export function translate(language: string, key: string, params?: Record<string, string>): string {
  const translation = translations[language]?.[key] || translations[defaultLanguage]?.[key] || key;

  if (params) {
    return Object.entries(params).reduce((acc, [paramKey, paramValue]) => {
      return acc.replace(new RegExp(`\\{${paramKey}\\}`, 'g'), paramValue);
    }, translation);
  }

  return translation;
}